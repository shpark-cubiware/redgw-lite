#!/bin/bash
# =============================================================================
# RedGW 헬스체크 테스트
# =============================================================================
#
# 목적: RedGW 서비스와 Redis 연결 상태를 확인합니다.
#
# 엔드포인트:
#   GET /health — 서비스 및 Redis 연결 상태 확인
#
# 정상 응답:
#   {"status": "healthy", "redis": "connected"}
#
# Redis 연결 실패 시:
#   {"status": "degraded", "redis": "disconnected"}
#
# 사용법:
#   bash tests/test_00_health.sh
#
# 참고:
#   - 이 엔드포인트는 인증 없이 접근 가능합니다
#   - Docker 기동 직후 약간의 지연이 있을 수 있습니다
#   - 모니터링/헬스체크 도구(k8s liveness probe 등)에서 활용
# =============================================================================
BASE="${REDGW_BASE_URL:-http://localhost:3080}"

echo "============================================"
echo " 헬스체크"
echo "============================================"

echo ""
echo "--- GET /health ---"
echo "# Redis 연결 상태를 포함한 서비스 상태 확인"
curl -s "$BASE/health" | python -m json.tool
